#include "liblinalg.h"

double	determinant_3x3(t_matrix *m)
{
	double	det;

	det = 0;
	det = (*m)[0][0] * ((*m)[1][1] * (*m)[2][2] - (*m)[1][2] * \
	(*m)[2][1]) - (*m)[0][1] * ((*m)[1][0] * (*m)[2][2] - (*m)[1][2] \
	* (*m)[2][0]) + (*m)[0][2] * ((*m)[1][0] * (*m)[2][1] - (*m)[1][1] \
	* (*m)[2][0]);
	return (det);
}
